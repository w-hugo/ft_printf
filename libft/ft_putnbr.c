#include "libft.h"

void		ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		n++;
		n = -n;
		ft_putchar('-');
		ft_putnbr(n / 10);
		ft_putnbr(n % 10 + 1);
		return ;
	}
	else if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
