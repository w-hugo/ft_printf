#include "libft.h"
#include <stdlib.h>

void			ft_strclr(char *s)
{
	int			i;
	int			len;

	i = -1;
	if (!s)
		return ;
	len = ft_strlen(s);
	while (++i < len)
		s[i] = '\0';
}
